// Fuzzy match GPU name to benchmark entry
export function findGPUBenchmark(gpuName) {
  if (!gpuName) return null;
  const n = gpuName.toUpperCase();

  // Direct match (longest key first for specificity)
  const keys = Object.keys(GPU_BENCHMARKS).sort((a, b) => b.length - a.length);
  for (const key of keys) {
    if (n.includes(key.toUpperCase())) {
      return { name: key, ...GPU_BENCHMARKS[key] };
    }
  }

  // Number-only fallback (e.g., "4070" from "MSI GeForce RTX 4070 GAMING X SLIM")
  for (const key of keys) {
    const nums = key.match(/\d{4}(?:\s*(?:Ti|XT|XTX|GRE|Super))?/i)?.[0];
    if (nums && n.includes(nums.toUpperCase())) {
      return { name: key, ...GPU_BENCHMARKS[key] };
    }
  }

  return null;
}
