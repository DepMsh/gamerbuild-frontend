// Get best price for a component
export function getBestPrice(componentId) {
  const prices = STORE_PRICES[componentId];
  if (!prices) return null;

  let best = { store: null, price: Infinity };
  for (const [store, price] of Object.entries(prices)) {
    if (price && price < best.price) {
      best = { store, price };
    }
  }
  return best.price === Infinity ? null : best;
}

// Get all store prices for a component (sorted cheapest first)
export function getStorePrices(componentId) {
  const prices = STORE_PRICES[componentId];
  if (!prices) return [];

  return Object.entries(prices)
    .filter(([, price]) => price !== null)
    .map(([storeKey, price]) => ({
      store: STORES.find(s => s.key === storeKey),
      price,
    }))
    .sort((a, b) => a.price - b.price);
}

// Get price range for a component
export function getPriceRange(componentId) {
  const storePrices = getStorePrices(componentId);
  if (!storePrices.length) return null;
  return {
    min: storePrices[0].price,
    max: storePrices[storePrices.length - 1].price,
    savings: storePrices[storePrices.length - 1].price - storePrices[0].price,
  };
}
